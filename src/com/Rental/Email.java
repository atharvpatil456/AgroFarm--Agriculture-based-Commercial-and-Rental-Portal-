package com.Rental;

public class Email{
	static String email;
	static String pname;
	static String pamt;
	static String name;
	/**
	 * @return the email
	 */
	public static String getEmail() {
		return email;
	}

	/**
	 * @param email the email to set
	 */
	public static void setEmail(String email) {
		Email.email = email;
	}
	
	public static String getPname() {
		return pname;
	}

	/**
	 * @param email the email to set
	 */
	public static void setPname(String pname) {
		Email.pname = pname;
	}
	
	public static String getPamt() {
		return pamt;
	}

	/**
	 * @param email the email to set
	 */
	public static void setPamt(String pamt) {
		Email.pamt = pamt;
	}

	public static String getName() {
		return name;
	}

	/**
	 * @param email the email to set
	 */
	public static void setName(String name) {
		Email.name = name;
	}
}
